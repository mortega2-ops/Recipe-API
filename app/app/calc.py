

def add(x, y):
    """A simple function that returns the sum of two integers
    Parameters
    ----------
    x : int, required
        The first number to be summed
    y : int, required
        The second number to be summed
    Returns
    -------
    int
        a sum of the numbers passed in
    """
    return x + y


def subtract(x, y):
    """A simple function that returns the difference of two integers
    Parameters
    ----------
    x : int, required
        The number to be subtracted
    y : int, required
        The number to be subtracted from
    Returns
    -------
    int
        a difference of the numbers passed in
    """
    return y - x
